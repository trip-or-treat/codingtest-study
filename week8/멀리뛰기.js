/*
    칸수   방법수    경우의 수
    1      1         1
    2      2         1,1 / 2 
    3      3         1,1,1 / 1,2 / 2,1
    4      5         1,1,1,1 / 1,2,1 / 2,1,1 / 1,1,2 / 2,2
    5      8         1,1,1,1,1 / 2,1,1,1 / 1,2,1,1 / 1,1,2,1 / 1,1,1,2 / 2,2,1 / 2,1,2 / 1,2,2    
    => DP의 전형적인 문제 
*/

function solution(n) {
  const ans = [1, 2, 3];

  for (let i = 3; i <= n; i++) {
    ans[i] = (ans[i - 1] + ans[i - 2]) % 1234567;
  }

  return ans[n - 1];
}
